#include "libft.h"

void	*ft_memrchr(const void *s, int c, size_t n)
{
	char	*s_c;

	s_c = (char *)s;
	while (n > 0)
	{
		if (s_c[n] == c)
		{
			return (&(s_c[n]));
		}
		n--;
	}
	return (NULL);
}
